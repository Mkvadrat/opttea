function translitIt($str) 
{
    $tr = array(
        ""=>"a",""=>"b",""=>"v",""=>"g",
        ""=>"d",""=>"e",""=>"j",""=>"z",""=>"i",
        ""=>"y",""=>"k",""=>"l",""=>"m",""=>"n",
        ""=>"o",""=>"p",""=>"r",""=>"s",""=>"t",
        ""=>"u",""=>"f",""=>"h",""=>"ts",""=>"ch",
        ""=>"sh",""=>"sch",""=>"",""=>"yi",""=>"",
        ""=>"e",""=>"yu",""=>"ya",""=>"a",""=>"b",
        ""=>"v",""=>"g",""=>"d",""=>"e",""=>"j",
        ""=>"z",""=>"i",""=>"y",""=>"k",""=>"l",
        ""=>"m",""=>"n",""=>"o",""=>"p",""=>"r",
        ""=>"s",""=>"t",""=>"u",""=>"f",""=>"h",
        ""=>"ts",""=>"ch",""=>"sh",""=>"sch",""=>"y",
        ""=>"yi",""=>"",""=>"e",""=>"yu",""=>"ya", 
        " "=> "_",  "/"=> "_", ''=> "", ''=> "", '?'=> ""
    );
    return strtr($str,$tr);
}

